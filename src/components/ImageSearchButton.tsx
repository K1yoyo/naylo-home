        // æ³¨æ„ï¼šè¿™é‡Œå¿…é¡»ç»™ Blob ä¸€ä¸ªæ–‡ä»¶åï¼Œå¦åˆ™åç«¯å¯èƒ½ä¸è®¤
        uploadForm.append("file", compressedBlob, "compressed_image.jpg");

        const uploadRes = await fetch("/api/upload", { method: "POST", body: uploadForm });
        
        // å°è¯•è§£æé”™è¯¯ä¿¡æ¯
        let uploadData;
